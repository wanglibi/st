package cn.model;

public class StockParam {
  private String kname;
  private String kvalue;
  private String kdesc;
  private String kgroup;
  private String korder;
  
  public String getKname() {
    return kname;
  }
  public void setKname(String kname) {
    this.kname = kname;
  }
  public String getKvalue() {
    return kvalue;
  }
  public void setKvalue(String kvalue) {
    this.kvalue = kvalue;
  }
  public String getKdesc() {
    return kdesc;
  }
  public void setKdesc(String kdesc) {
    this.kdesc = kdesc;
  }
  public String getKgroup() {
    return kgroup;
  }
  public void setKgroup(String kgroup) {
    this.kgroup = kgroup;
  }
  public String getKorder() {
    return korder;
  }
  public void setKorder(String korder) {
    this.korder = korder;
  }
}
